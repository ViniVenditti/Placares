package com.venditti.placares.model;

public class Points {

    private int doing;
    private int done;
    private int summation;

    public int getDoing() {
        return doing;
    }

    public void setDoing(int doing) {
        this.doing = doing;
    }

    public int getDone() {
        return done;
    }

    public void setDone(int done) {
        this.done = done;
    }

    public int getSummation() {
        return summation;
    }

    public void setSummation(int summation) {
        this.summation = summation;
    }
}
